--SEQUENCE 생성하기
CREATE SEQUENCE DEPT_DEPTNO_SEQ
INCREMENT BY 10
START WITH 10;

--생성된 SEQUENCE 객체 사용
--NEXTVAL, CURRVAL 명령어 사용
SELECT DEPT_DEPTNO_SEQ.NEXTVAL FROM DUAL;

SELECT DEPT_DEPTNO_SEQ.CURRVAL FROM DUAL;

