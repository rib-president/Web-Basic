package p01.basic;

//	String의 길이 구하기
public class StringBasic2 {
	public static void main(String[] args) {
		String message = "Welcome to Java";
		String message2 = "안녕하세요, 자바씨!!";
		
		System.out.println(message + " 변수의 길이는 " + message.length()
		 + "입니다.");
		System.out.println(message2 + " 변수의 길이는 " + message2.length()
		 + "입니다.");
	}
}
